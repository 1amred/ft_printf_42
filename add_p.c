#include "ft_printf.h"

int	add_p(unsigned long int nb, int *i)
{
	char	*str;

	str = "0123456789abcdef";
	if (nb >= 16)
	{
		add_p(nb / 16, i);
	}
	write (1, &str[nb % 16], 1);
	*i += 1;
	return (*i);
}
