#include "ft_printf.h"

int	hexa_dp(unsigned int nb, int *i)
{
	char	*str;

	str = "0123456789ABCDEF";
	if (nb >= 16)
	{
		hexa_dp(nb / 16, i);
	}
	write (1, &str[nb % 16], 1);
	*i += 1;
	return (*i);
}
